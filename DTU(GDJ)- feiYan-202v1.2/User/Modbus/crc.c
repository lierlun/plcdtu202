/********************************************************************************************************************
** 函数名称: calCRC16()				
** 功能描述: 计算CRC16/XMODEM							
** 输　  入: INT8U *pSource: 数据					
			 INT16U len    : 长度						
** 输 　 出: CRC16码						
********************************************************************************************************************/
uint16_t calCRC16(uint8_t *pSource, uint32_t uiLen)
{
	uint16_t i = 0;
	uint16_t uiResult = 0;
	uint8_t  p;
	for (i=0; i<uiLen; i++)
	{
		  p = pSource[i];
	          uiResult = (uiResult<<8) ^ ((uint16_t)CRCTable[(uiResult>>8) ^ p]);
	}
	return uiResult;
}

/********************************************************************************************************************
** 函数名称: calCRC16_modbus()				
** 功能描述: 计算CRC16/MODBUS							
** 输　  入: INT8U *updata: 数据					
			 INT16U len    : 长度						
** 输 　 出: CRC16码						
********************************************************************************************************************/

uint16_t calCRC16_modbus(uint8_t *updata,uint16_t len)
{
  uint8_t   uchCRCHi=0xff;
  uint8_t   uchCRCLo=0xff;
  uint16_t  uindex;
  while(len--)
  {
    uindex=uchCRCHi^*updata++;
    uchCRCHi=uchCRCLo^auchCRCHi[uindex];
    uchCRCLo=auchCRCLo[uindex];
  }
  return (uchCRCHi<<8|uchCRCLo);
}

